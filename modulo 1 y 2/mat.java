public class mat{
    public static void main(String[] args) {
        int x = 10;
        int y = 5;
        System.out.println(x + " + " + y  + " = " + (x+y));
        System.out.println(x + " - " + y  + " = " + (x-y));
        System.out.println(x + " x " + y  + " = " + (x*y));
        System.out.println(x + " / " + y  + " = " + (x/y));
    }
}